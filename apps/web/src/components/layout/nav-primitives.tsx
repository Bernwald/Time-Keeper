// ─── Types ─────────────────────────────────────────────────────────────

export type NavItem = {
  href: string;
  label: string;
  icon: ComponentType<{ size?: number }>;
};

export type NavGroup = {
  label: string;
  items: NavItem[];
};

// ─── Reusable building blocks ─────────────────────────────────────────

export function NavLink({
  item,
  exact,
}: {
  item: NavItem;
  exact?: boolean;
}) {
  const pathname = usePathname();
  const active = exact
    ? pathname === item.href
    : pathname === item.href || pathname.startsWith(item.href + "/");
  const Icon = item.icon;

  return (
    <Link
      href={item.href}
      className="flex items-center gap-2.5 min-h-[40px] px-3 rounded-xl text-[13px] font-medium transition-all"
      style={{
        background: active ? "var(--color-accent-soft)" : "transparent",
        color: active ? "var(--color-accent)" : "var(--color-muted)",
        fontWeight: active ? 600 : 500,
        transition: `all var(--duration-fast) var(--ease-out)`,
      }}
      onMouseEnter={(e) => {
        if (!active) {
          e.currentTarget.style.background = "var(--color-bg-elevated)";
          e.currentTarget.style.color = "var(--color-text)";
        }
      }}
      onMouseLeave={(e) => {
        if (!active) {
          e.currentTarget.style.background = "transparent";
          e.currentTarget.style.color = "var(--color-muted)";
        }
      }}
    >
      <Icon size={18} />
      {item.label}
    </Link>
  );
}

export function NavGroupBlock({ group }: { group: NavGroup }) {
  return (
    <div>
      <p
        className="text-[11px] font-semibold uppercase tracking-widest mb-1.5 px-3"
        style={{ color: "var(--color-placeholder)" }}
      >
        {group.label}
      </p>
      <ul className="flex flex-col gap-0.5">
        {group.items.map((item) => (
          <li key={item.href}>
            <NavLink item={item} />
          </li>
        ))}
      </ul>
    </div>
  );
}

